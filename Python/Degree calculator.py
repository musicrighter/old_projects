x = input('Do you want Fahrenheit (f), or Celsius (c)?: ')

if x == ('c'):
    c = float(input('What is the temperature in Fahrenheit?: '))
    print('The temperature in Celsius is', 5*(c-32)/9)

if x == ('f'):
    f = float(input('What is the temperature in Celsius?: '))
    print('The temperature in Fahrenheit is', (9*f/5)+32)
    
        





    
